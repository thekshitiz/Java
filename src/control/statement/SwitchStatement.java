package control.statement;

public class SwitchStatement {
    public static void main(String[] args) {

        int day = 2;
        switch (day) {
            case 1:
                System.out.println("gooday");
                break;
            case 2:
                System.out.println("bestday");
                break;
            case 3:
                System.out.println("friday");
                break;
        }

    }

}